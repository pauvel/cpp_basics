#include <iostream>
#include <cmath>

int main(){
    
    std::cout << 8+8 << std::endl;
    std::cout << 8-8 << std::endl;
    std::cout << 8*8 << std::endl;
    std::cout << 8%3 << std::endl;
    std::cout << (8+2)*3 << std::endl;
    std::cout << 5.1+6.3 << std::endl;
    std::cout << 5.0/4 << std::endl;
    
    
    std::cout << pow(2, 8) << std::endl;
    std::cout << round(2.25) << std::endl;
    std::cout << ceil(2.75) << std::endl;
    std::cout << floor(2.75) << std::endl;
    
    
    
    
    
    
    return 0;
}
